class Solution {
  public:
    int pairsum(vector<int> &arr) {
        // code here
        int ans,tp;
        ans=tp=INT_MIN;
        for(auto it:arr){
            if(ans<it){
                if(tp<ans)tp=ans;
                ans=it;
            }
            if(it<ans && it>tp)tp=it;
        }
        if(tp==INT_MIN)return -1;
        return ans+tp;
    }
};


